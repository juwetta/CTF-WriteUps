def is_straight_line(points):
    if len(points) < 2:
        return True

    (x0, y0), (x1, y1) = points[:2]
    for (x, y) in points[2:]:
        if (x1 - x0) * (y - y0) != (y1 - y0) * (x - x0):
            return False
    return True


